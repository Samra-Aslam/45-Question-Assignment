//Equality with string .
console.log("testing equality with string;");
console.log("apple" == "apple");
//using lowercase function.
console.log("testing lower case function;");
console.log("Apple".toLowerCase() == "apple");
//Numerical test.
console.log("testing equality with numerical test;");
console.log(10 > 5);
console.log(2 < 1);
console.log(20 >= 20);
console.log(10 <= 15);
//test using "and" and "or" operators
console.log(true && false); //false
console.log(true || false); //true
//testing whether an item in array
var fruite = ["apple", "mango"];
console.log("is apple in fruite");
console.log(fruite.includes("apple")); //true
//Testing whether an item is not in array
console.log(fruite.includes("graps"));
